  translateDynamicText(text, lang) {
    if (lang === "tr") return text;
    let output = text;
    this.phraseMap.forEach(([tr, en]) => {
      output = output.replace(new RegExp(tr, "g"), en);
    });
    output = output
      .split(/\b/)
      .map((token) => {
        const key = token.toLowerCase();
        if (!this.wordMap[key]) return token;
        return token[0] === token[0]?.toUpperCase()
          ? this.wordMap[key].charAt(0).toUpperCase() + this.wordMap[key].slice(1)
          : this.wordMap[key];
      })
      .join("")
      .replace(/\s+/g, " ")
      .trim();
    return output;
  }
